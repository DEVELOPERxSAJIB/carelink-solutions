import { rootApi } from "./RootApi";

export const EmergencyApi = rootApi.injectEndpoints({
  endpoints: (builder) => ({
    getEmergencyById: builder.query({
      query: (emergencyId) => `emergency/${emergencyId}`,
      providesTags: (result, error, emergencyId) => [{ type: "Emergency", id: emergencyId }],
    }),
    createEmergency: builder.mutation({
      query: (emergencyData) => ({
        url: "emergency",
        method: "POST",
        body: emergencyData,
      }),
      invalidatesTags: [{ type: "Emergency" }],
    }),
    getAllEmergencies: builder.query({
      query: () => ({
        url: "emergency",
        method: "GET",
      }),
      providesTags: (result, error, _) => [{ type: "Emergency" }],
    }),
    updateEmergency: builder.mutation({
      query: ({ emergencyId, emergencyData }) => ({
        url: `emergency/${emergencyId}`,
        method: "PUT",
        body: emergencyData,
      }),
      invalidatesTags: (result, error, { emergencyId }) => [
        { type: "Emergency", id: emergencyId },
      ],
    }),
    deleteEmergency: builder.mutation({
      query: (emergencyId) => ({
        url: `emergency/${emergencyId}`,
        method: "DELETE",
      }),
      invalidatesTags: (result, error, emergencyId) => [
        { type: "Emergency", id: emergencyId },
      ],
    }),
  }),
});

// Export hooks generated by the endpoints
export const {
  useGetEmergencyByIdQuery,
  useCreateEmergencyMutation,
  useGetAllEmergenciesQuery,
  useUpdateEmergencyMutation,
  useDeleteEmergencyMutation,
} = EmergencyApi;
